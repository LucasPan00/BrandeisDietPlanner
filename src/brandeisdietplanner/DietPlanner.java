    public ArrayList<String> readFile(File f) throws FileNotFoundException {
        Scanner sc = new Scanner(f);
        ArrayList<String> result = new ArrayList<String>();
        while (sc.hasNextLine()) {
            result.add(sc.nextLine());
        }
        return result;
    }

